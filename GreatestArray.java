public class GreatestArray {
    public static void main(String[] args) {
        int[]no ={3,2,5};
        int i =0;
        int max=no[i];
        while (i<no.length)
         {
            i++;
            if(no[i]>max)
         
            {
                max=no[i];
            }
        }


    }
}
